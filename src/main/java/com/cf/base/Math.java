public class Math {

    public static void main(String[] args) {
        System.out.println(1 & 2);
        System.out.println(1 & 3);
        System.out.println(1 | 2);
        System.out.println(1 | 3);
        System.out.println(1 ^ 2);
        System.out.println(1 ^ 3);
        System.out.println(~ 3);
        System.out.println(-2 >>> 2);
    }
}
